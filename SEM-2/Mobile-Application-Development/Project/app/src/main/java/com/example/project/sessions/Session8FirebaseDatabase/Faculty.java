package com.example.project.sessions.Session8FirebaseDatabase;

public class Faculty {
    public String empId, email, name, password, designation;

    public Faculty() {}

    public Faculty(String empId, String email, String name, String password, String designation) {
        this.empId = empId;
        this.email = email;
        this.name = name;
        this.password = password;
        this.designation = designation;
    }
}
