package com.example.project.sessions.Session8FirebaseDatabase;

public class Alumni {
    public String alumniId, email, name, password, passingYear;

    public Alumni() {}

    public Alumni(String alumniId, String email, String name, String password, String passingYear) {
        this.alumniId = alumniId;
        this.email = email;
        this.name = name;
        this.password = password;
        this.passingYear = passingYear;
    }
}